from __future__ import annotations

import argparse
import json
import sys
from collections import Counter
from dataclasses import asdict, dataclass
from pathlib import Path
from typing import Sequence

try:
    from scripts.validate_templates import table_blocks, table_cells
except ModuleNotFoundError:
    from validate_templates import table_blocks, table_cells


BLANK_STATUS = "(blank)"
GENERATED_MARKER = "<!-- generated by scripts/summarize_status.py -->"


@dataclass(frozen=True)
class StatusRecord:
    source: str
    line: int
    item: str
    status: str
    column: str


@dataclass(frozen=True)
class ColumnStatusPolicy:
    column: str
    allowed_statuses: tuple[str, ...]
    blocking_statuses: tuple[str, ...]
    require_status: bool


@dataclass(frozen=True)
class StatusPolicy:
    allowed_statuses: tuple[str, ...]
    blocking_statuses: tuple[str, ...]
    require_status: bool
    column_rules: tuple[ColumnStatusPolicy, ...] = ()


@dataclass(frozen=True)
class PolicyViolation:
    source: str
    line: int
    item: str
    status: str
    column: str
    reason: str


def normalize_heading(value: str) -> str:
    return " ".join(value.casefold().split())


def status_column_index(header: list[str]) -> int | None:
    matches = [
        index
        for index, cell in enumerate(header)
        if normalize_heading(cell) == "status"
        or normalize_heading(cell).endswith(" status")
    ]
    if len(matches) > 1:
        raise ValueError("table has more than one status column")
    return matches[0] if matches else None


def is_status_legend(header: list[str]) -> bool:
    normalized = [normalize_heading(cell) for cell in header]
    return (
        len(normalized) == 2
        and normalized[0] == "status"
        and normalized[1]
        in {
            "definition",
            "meaning",
        }
    )


def extract_status_records(path: Path) -> list[StatusRecord]:
    lines = path.read_text(encoding="utf-8").splitlines()
    if lines and lines[0].strip() == GENERATED_MARKER:
        return []
    records: list[StatusRecord] = []
    for start_line, block in table_blocks(lines):
        if len(block) < 3:
            continue
        header = table_cells(block[0])
        if is_status_legend(header):
            continue
        status_index = status_column_index(header)
        if status_index is None:
            continue

        for offset, row in enumerate(block[2:], start=2):
            cells = table_cells(row)
            row_line = start_line + offset
            if len(cells) != len(header):
                raise ValueError(
                    f"{path}:{row_line}: table row has {len(cells)} cells; "
                    f"expected {len(header)}"
                )
            item = next(
                (
                    cell
                    for index, cell in enumerate(cells)
                    if index != status_index and cell
                ),
                f"Row {row_line}",
            )
            records.append(
                StatusRecord(
                    source=path.as_posix(),
                    line=row_line,
                    item=item,
                    status=cells[status_index] or BLANK_STATUS,
                    column=header[status_index],
                )
            )
    return records


def expand_paths(inputs: Sequence[Path]) -> list[Path]:
    paths: list[Path] = []
    for input_path in inputs:
        if input_path.is_dir():
            paths.extend(sorted(input_path.glob("*.md")))
        elif input_path.is_file():
            paths.append(input_path)
        else:
            raise FileNotFoundError(f"input does not exist: {input_path}")

    unique: list[Path] = []
    seen: set[Path] = set()
    for path in paths:
        resolved = path.resolve()
        if resolved not in seen:
            seen.add(resolved)
            unique.append(path)
    return unique


def _policy_statuses(
    document: dict[str, object], key: str, context: str
) -> tuple[str, ...]:
    raw_statuses = document.get(key, [])
    if not isinstance(raw_statuses, list) or any(
        not isinstance(status, str) or not status.strip() for status in raw_statuses
    ):
        raise ValueError(f"{context} {key} must be a list of nonempty strings")
    statuses = tuple(status.strip() for status in raw_statuses)
    normalized = [status.casefold() for status in statuses]
    if len(normalized) != len(set(normalized)):
        raise ValueError(f"{context} {key} contains case-insensitive duplicates")
    return statuses


def _parse_status_rule(
    document: dict[str, object], context: str
) -> tuple[tuple[str, ...], tuple[str, ...], bool]:
    allowed_keys = {"allowed_statuses", "blocking_statuses", "require_status"}
    unknown_keys = sorted(set(document) - allowed_keys)
    if unknown_keys:
        raise ValueError(f"{context} has unknown fields: " + ", ".join(unknown_keys))

    require_status = document.get("require_status", False)
    if not isinstance(require_status, bool):
        raise ValueError(f"{context} require_status must be true or false")
    allowed_statuses = _policy_statuses(document, "allowed_statuses", context)
    blocking_statuses = _policy_statuses(document, "blocking_statuses", context)
    if allowed_statuses:
        allowed = {status.casefold() for status in allowed_statuses}
        outside_allowlist = [
            status for status in blocking_statuses if status.casefold() not in allowed
        ]
        if outside_allowlist:
            raise ValueError(
                f"{context} blocking_statuses must also be allowed: "
                + ", ".join(outside_allowlist)
            )
    return allowed_statuses, blocking_statuses, require_status


def load_status_policy(path: Path) -> StatusPolicy:
    """Load global and column-specific readiness status rules."""

    document = json.loads(path.read_text(encoding="utf-8"))
    if not isinstance(document, dict):
        raise ValueError("status policy root must be a JSON object")
    allowed_keys = {
        "allowed_statuses",
        "blocking_statuses",
        "require_status",
        "column_rules",
    }
    unknown_keys = sorted(set(document) - allowed_keys)
    if unknown_keys:
        raise ValueError("status policy has unknown fields: " + ", ".join(unknown_keys))

    global_document = {
        key: value for key, value in document.items() if key != "column_rules"
    }
    allowed_statuses, blocking_statuses, require_status = _parse_status_rule(
        global_document,
        "policy",
    )

    raw_column_rules = document.get("column_rules", {})
    if not isinstance(raw_column_rules, dict):
        raise ValueError("policy column_rules must be a JSON object")
    column_rules: list[ColumnStatusPolicy] = []
    normalized_columns: set[str] = set()
    for raw_column, raw_rule in raw_column_rules.items():
        if not isinstance(raw_column, str) or not raw_column.strip():
            raise ValueError("policy column_rules keys must be nonempty strings")
        column = raw_column.strip()
        normalized_column = normalize_heading(column)
        if normalized_column in normalized_columns:
            raise ValueError(
                "policy column_rules contains case-insensitive duplicate columns"
            )
        normalized_columns.add(normalized_column)
        if not isinstance(raw_rule, dict):
            raise ValueError(f"policy column rule {column!r} must be a JSON object")
        required_rule_fields = {
            "allowed_statuses",
            "blocking_statuses",
            "require_status",
        }
        missing_rule_fields = sorted(required_rule_fields - set(raw_rule))
        if missing_rule_fields:
            raise ValueError(
                f"policy column rule {column!r} is missing fields: "
                + ", ".join(missing_rule_fields)
            )
        rule_allowed, rule_blocking, rule_required = _parse_status_rule(
            raw_rule,
            f"policy column rule {column!r}",
        )
        column_rules.append(
            ColumnStatusPolicy(
                column=column,
                allowed_statuses=rule_allowed,
                blocking_statuses=rule_blocking,
                require_status=rule_required,
            )
        )
    return StatusPolicy(
        allowed_statuses=allowed_statuses,
        blocking_statuses=blocking_statuses,
        require_status=require_status,
        column_rules=tuple(column_rules),
    )


def evaluate_status_policy(
    records: Sequence[StatusRecord], policy: StatusPolicy
) -> list[PolicyViolation]:
    """Return line-level blank, unknown, and blocking status violations."""

    column_rules = {
        normalize_heading(rule.column): rule for rule in policy.column_rules
    }
    violations: list[PolicyViolation] = []
    for record in records:
        selected_rule = column_rules.get(normalize_heading(record.column), policy)
        allowed = {status.casefold() for status in selected_rule.allowed_statuses}
        blocking = {status.casefold() for status in selected_rule.blocking_statuses}
        normalized = record.status.casefold()
        reason = None
        if record.status == BLANK_STATUS:
            if selected_rule.require_status:
                reason = "status is required"
        elif allowed and normalized not in allowed:
            reason = "status is not in allowed_statuses"
        elif normalized in blocking:
            reason = "status is blocking"
        if reason:
            violations.append(
                PolicyViolation(
                    source=record.source,
                    line=record.line,
                    item=record.item,
                    status=record.status,
                    column=record.column,
                    reason=reason,
                )
            )
    return violations


def summarize_records(records: Sequence[StatusRecord]) -> dict[str, object]:
    counts = Counter(record.status for record in records)
    ordered_counts = {
        status: counts[status]
        for status in sorted(counts, key=lambda value: value.casefold())
    }
    return {
        "source_files": sorted({record.source for record in records}),
        "row_count": len(records),
        "status_counts": ordered_counts,
        "items": [asdict(record) for record in records],
    }


def _markdown_cell(value: object) -> str:
    return str(value).replace("|", "\\|").replace("\n", " ")


def render_markdown(summary: dict[str, object]) -> str:
    source_files = summary["source_files"]
    status_counts = summary["status_counts"]
    items = summary["items"]
    lines = [
        GENERATED_MARKER,
        "<!-- markdownlint-disable MD013 -->",
        "",
        "# Readiness Status Summary",
        "",
        f"Source files: {len(source_files)}  ",
        f"Status rows: {summary['row_count']}",
        "",
        "## Counts",
        "",
        "| Status | Count |",
        "| --- | ---: |",
    ]
    lines.extend(
        f"| {_markdown_cell(status)} | {count} |"
        for status, count in status_counts.items()
    )
    if "status_policy" in summary:
        policy = summary["status_policy"]
        violations = summary["policy_violations"]
        lines.extend(
            [
                "",
                "## Policy Evaluation",
                "",
                f"Policy source: {_markdown_cell(policy['source'])}  ",
                f"Violations: {len(violations)}",
            ]
        )
        if violations:
            lines.extend(
                [
                    "",
                    "| Source | Line | Item | Status | Column | Reason |",
                    "| --- | ---: | --- | --- | --- | --- |",
                ]
            )
            lines.extend(
                "| {source} | {line} | {item} | {status} | {column} | {reason} |".format(
                    **{key: _markdown_cell(value) for key, value in violation.items()}
                )
                for violation in violations
            )
    lines.extend(
        [
            "",
            "## Items",
            "",
            "| Source | Line | Item | Status |",
            "| --- | ---: | --- | --- |",
        ]
    )
    lines.extend(
        "| {source} | {line} | {item} | {status} |".format(
            source=_markdown_cell(item["source"]),
            line=item["line"],
            item=_markdown_cell(item["item"]),
            status=_markdown_cell(item["status"]),
        )
        for item in items
    )
    return "\n".join(lines) + "\n"


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Summarize status columns from filled BESS Markdown tables."
    )
    parser.add_argument("paths", nargs="*", type=Path)
    parser.add_argument("--format", choices=("markdown", "json"), default="markdown")
    parser.add_argument("--output", type=Path)
    parser.add_argument(
        "--policy",
        type=Path,
        help="JSON policy with global and column-specific status rules",
    )
    parser.add_argument(
        "--fail-on",
        action="append",
        default=[],
        metavar="STATUS",
        help="exit 2 when this status occurs; repeat for multiple states",
    )
    return parser


def main(argv: Sequence[str] | None = None) -> int:
    args = build_parser().parse_args(argv)
    try:
        inputs = args.paths or [Path("templates")]
        paths = expand_paths(inputs)
        records = [record for path in paths for record in extract_status_records(path)]
        policy = load_status_policy(args.policy) if args.policy else None
    except (OSError, ValueError) as error:
        print(f"Status summary failed: {error}", file=sys.stderr)
        return 1

    if not records:
        print("Status summary failed: no status-bearing rows found.", file=sys.stderr)
        return 1

    summary = summarize_records(records)
    policy_violations: list[PolicyViolation] = []
    if policy:
        policy_violations = evaluate_status_policy(records, policy)
        summary["status_policy"] = {
            "source": args.policy.as_posix(),
            "allowed_statuses": policy.allowed_statuses,
            "blocking_statuses": policy.blocking_statuses,
            "require_status": policy.require_status,
            "column_rules": {
                rule.column: {
                    "allowed_statuses": rule.allowed_statuses,
                    "blocking_statuses": rule.blocking_statuses,
                    "require_status": rule.require_status,
                }
                for rule in policy.column_rules
            },
        }
        summary["policy_violations"] = [
            asdict(violation) for violation in policy_violations
        ]
    if args.format == "json":
        rendered = json.dumps(summary, indent=2) + "\n"
    else:
        rendered = render_markdown(summary)

    if args.output:
        args.output.parent.mkdir(parents=True, exist_ok=True)
        args.output.write_text(rendered, encoding="utf-8")
    else:
        print(rendered, end="")

    blocked = {status.casefold() for status in args.fail_on}
    matched = sorted(
        {record.status for record in records if record.status.casefold() in blocked},
        key=str.casefold,
    )
    if matched:
        print(
            "Status summary gate failed on: " + ", ".join(matched),
            file=sys.stderr,
        )
    for violation in policy_violations:
        print(
            f"{violation.source}:{violation.line}: {violation.item}: "
            f"{violation.reason} ({violation.status}; column: {violation.column})",
            file=sys.stderr,
        )
    if policy_violations:
        print(
            f"Status policy gate failed with {len(policy_violations)} violation(s).",
            file=sys.stderr,
        )
    if matched or policy_violations:
        return 2
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
